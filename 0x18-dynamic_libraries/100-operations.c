#include <stdio.h>


/**
 * add - add two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: sum of the two integers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: difference between two integers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: product of the two integers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: division result of the two integers
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Finds the modulo of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: sum of the two integers
 */

int mod(int a, int b)
{
	return (a % b);
}
